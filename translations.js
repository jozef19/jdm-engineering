// Funkcia na prepínanie jazykov
function setLanguage(lang) {
  document.querySelectorAll('[data-translate]').forEach(el => {
    const key = el.dataset.translate;
    el.innerHTML = translations[lang][key] || translations['sk'][key] || '';
  });

  // Zmeniť aj <title>
  const titleEl = document.querySelector('title[data-translate]');
  if(titleEl) {
    const pageKey = titleEl.dataset.translate;
    titleEl.innerHTML = translations[lang][pageKey] || translations['sk'][pageKey] || '';
  }

  // Uloženie výberu jazyka
  localStorage.setItem('selectedLanguage', lang);
}

// Nastavenie jazyka pri načítaní stránky a pripojenie tlačidiel
document.addEventListener('DOMContentLoaded', () => {
  // 1. Zisti uložený jazyk
  let lang = localStorage.getItem('selectedLanguage');

  // 2. Ak nie je uložený, použije sa jazyk prehliadača
  if(!lang || !['sk','en','cz','de','pl'].includes(lang)){
    lang = navigator.language.slice(0,2).toLowerCase();
    if(!['sk','en','cz','de','pl'].includes(lang)) lang = 'sk';
  }

  // 3. Nastavenie jazyka
  setLanguage(lang);

  // 4. Pripojenie tlačidiel na prepínanie jazykov
  document.querySelectorAll('.lang-switch button').forEach(btn => {
    btn.addEventListener('click', () => setLanguage(btn.dataset.lang));
  });
});
